% --- Executes on button press in pushbutton3.
function pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
[filename, pathname] = uigetfile('*.*', 'Pick a video file');

handles.vidObj = VideoReader([pathname filename]);

% set listbox 
numFrames = int32(handles.vidObj.Duration * handles.vidObj.FrameRate);
disp(handles.vidObj.Duration)
disp(handles.vidObj.FrameRate)
disp(numFrames)

listboxCell = cell(1,numFrames);
for i = 1:numFrames
    listboxCell{i} = ['frame' sprintf('%04i.png',i)];
end
handles.listbox1.String = listboxCell;

handles.im = read(handles.vidObj, 1);

axes(handles.axes1);
imshow(handles.im)
xlabel('frame 1');


guidata(hObject, handles);




